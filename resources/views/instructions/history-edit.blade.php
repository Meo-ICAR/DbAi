<h3>Edit Query</h3>

<p>This page allows you to modify an existing database query. Here's what you can do:</p>

<ul>
    <li><strong>Update Query Details</strong>: Modify the query name, type, or description</li>
    <li><strong>Edit SQL</strong>: Update the SQL query directly in the editor</li>
    <li><strong>Test Changes</strong>: Verify your changes with the "Test Query" button</li>
    <li><strong>Save or Cancel</strong>: Save your changes or discard them</li>
</ul>

<h4>Tips:</h4>
<ul>
    <li>Use the query builder for complex modifications by clicking "Switch to Query Builder"</li>
    <li>Add comments to document your changes for future reference</li>
    <li>Consider creating a new version instead of modifying existing queries that are in use</li>
</ul>
