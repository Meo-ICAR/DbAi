<h3>Query Details</h3>

<p>This page shows the detailed view of a specific query from your history. You can see:</p>

<ul>
    <li><strong>Query Details</strong>: The original query text and when it was executed</li>
    <li><strong>SQL Generated</strong>: The SQL that was executed against your database</li>
    <li><strong>Results</strong>: The data returned by the query (if any)</li>
    <li><strong>Execution Details</strong>: Performance metrics and execution statistics</li>
</ul>

<h4>Available Actions:</h4>
<ul>
    <li><strong>Rerun Query</strong>: Execute the same query again</li>
    <li><strong>Edit & Rerun</strong>: Modify the query before running it again</li>
    <li><strong>Save as Favorite</strong>: Bookmark this query for quick access</li>
    <li><strong>Export Results</strong>: Download the query results in various formats</li>
</ul>
