<h3>Query History</h3>

<p>This page displays your saved query history. Here's what you can do:</p>

<ul>
    <li><strong>View past queries</strong> with their execution times and status</li>
    <li><strong>Search</strong> through your history using the search bar</li>
    <li><strong>Filter</strong> queries by date, status, or type</li>
    <li><strong>Click on any query</strong> to view its details and results</li>
    <li>Use the <strong>actions menu</strong> to rerun, edit, or delete queries</li>
</ul>

<h4>Tips:</h4>
<ul>
    <li>Use the star icon to bookmark important queries</li>
    <li>You can export your query history using the export button</li>
    <li>Hover over query status indicators to see more details</li>
</ul>
