<h3>Create New Query</h3>

<p>This page allows you to create a new database query. Here's how to use it:</p>

<ul>
    <li><strong>Query Name</strong>: Enter a descriptive name for your query</li>
    <li><strong>Database Connection</strong>: Select the database connection to use</li>
    <li><strong>Query Type</strong>: Choose between SELECT, INSERT, UPDATE, or DELETE</li>
    <li><strong>Query Text</strong>: Enter your SQL query or use the visual query builder</li>
    <li><strong>Description</strong>: Add any notes or context about this query</li>
</ul>

<h4>Tips:</h4>
<ul>
    <li>Use the query builder for complex queries by clicking "Switch to Query Builder"</li>
    <li>Test your query before saving using the "Test Query" button</li>
    <li>Use the formatting tools to make your query more readable</li>
</ul>
